var selectTheme = document.getElementById("theme");

if (selectTheme.value = "original")
{
    document.body.style.backgroundColor = "#03031a";
    document.body.style.color =" #708090";
} else if (selectTheme.value = "landscape")
{
    document.body.style.background = "url('images/forest.jpg')";
    document.body.style.color = white;

}